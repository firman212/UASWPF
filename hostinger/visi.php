		<fieldset> <legend> <b> MISI : </b> </legend>
		 
		&nbsp;&nbsp;&nbsp;Untuk mewujudkan visi, sekolah menentukan langkah-langkah strategi yang dinyatakan dalam misi sebagai berikut : <br>

		&nbsp; 1. Meningkatkan prestasi akademik dan non akademik melalui peningkatan mutu pembelajaran dan sarana pembelajaran.<br>
		&nbsp; 2. Melaksanakan proses pembelajaran yang berorientasi pada peningkatan kemampuan individual secara proporsional.<br>
		&nbsp; 3. Meningkatkan dan mengembangkan efisiensi pembelajaran baik secara Lokal, Nasional maupun Internasional <br>
		&nbsp; 4. Menumbuh kembangkan budaya berprestasi pada warga sekolah. <br>
		&nbsp; 5. Membekali siswa pengetahuan dalam kegiatan olimpiade baik Lokal, Nasional maupun Internasional.<br>
		&nbsp; 6. Mengembangkan kemampuan peserta didik dalam berfikir logis, kreatif, inovatif, berprakarsa dan mandiri.<br>
		&nbsp; 7. Meningkatkan keterampilan dan apresiasi peserta didik di bidang ilmu pengetahuan, teknologi, sosial budaya dan seni <br> 
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;		melalui “Constructivisme  Learning” dan interaksi global. <br>
		&nbsp; 8. Menjadikan disiplin sebagai suatu budaya dan pola tingkah laku dalam semua kegiatan di lingkungan sekolah. <br>
		&nbsp; 9. Menjadikan sekolah sebagai pusat kegiatan, pelestarian dan pengembangan budaya. <br>
		&nbsp;10. Memiliki kemampuan mengapresiasikan seni dan budaya baik lokal, Nasional maupun Internasional. <br>
		&nbsp;11. Memupuk semangat nasionalisme untuk mempertahankan dan menjaga kedaulatan Negara <br>
		&nbsp;12. Meningkatkan dan memupuk wawasan kebangsaan melalui kegiatan peringatan hari besar Nasional dan Pelatihan Prabela Negara. <br>
		&nbsp;13. Mewujudkan pelestarian fungsi lingkungan, mencegah pencemaran, kerusakan lingkungan dan budaya bersih, sehat <br>
		&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;     serta lingkungan hijau ( clean and green) di tingkat Kabupaten, Propinsi, dan Nasional.<br>
		&nbsp;14. Memberdayakan potensi kecerdasan siswa dalam IPTEK, IMTAQ dan berorientasi kepada daya saing global.<br>
		&nbsp;15. Mengembangkan etos kerja dan profesionalitas penyelenggaraan dan pelayanan pendidikan.<br>
		&nbsp;16. Mengembangkan layanan pendidikan berbasis teknologi informasi untuk peningkatan mutu penyelenggaraan <br>
		&nbsp;&nbsp;&nbsp;	&nbsp;&nbsp; dan pelayanan pendidikan yang efektif dan efisien.
 
