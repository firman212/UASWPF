			<p>  &nbsp; &nbsp; &nbsp; &nbsp;Sekitar seribu orang dari berbagai elemen memadati Pendopo Shaba Swagata Blambangan yang menjadi lokasi acara. 
			
			Selain para pejabat forum pimpinan daerah, TNI, polisi, tokoh lintas agama, ormas, organisasi kepemudaan, partai politik, 
			
			mahasiswa dan juga pelajar tampak berbaur bersama mengenakan ikat kepala merah putih sembari mengibarkan bendera merah putih kecil.

			Meski gerakan nasional, namun pelaksanaan Nusantara Bersatu berbeda satu daerah dengan daerah yang lain. <br>
			
			&nbsp; &nbsp;&nbsp; &nbsp; Ada yang mengekspresikannya dengan apel, kirab dan lain sebagainya, namun di Banyuwangi

			menampilkan atraksi budaya sebagai ekspresi persatuan. Para hadirin yang memenuhi pendopo Banyuwangi tersebut disuguhi 
			
			berbagai jenis tari-tarian dari berbagai daerah di Indonesia. Tak ketinggalan berbagai nyanyian daerah yang dibawakan 
			
			oleh paduan suara SMAN 1 Banyuwangi juga turut mengiringi. “Jika di daerah lain ada yang melaksanakan apel, kirab, 
			
			konvoi dan lain sebagainya, tapi kita di Banyuwangi mengekspresikannya dengan atraksi 
			
			kebudayaan,” ujar Bupati Banyuwangi Abdullah Azwar Anas saat m]embuka acara tersebut di Pendopo, Rabu (30/11). <br>
			
			&nbsp; &nbsp;&nbsp; &nbsp; Dengan kebudayaan itu, papar Anas, berbagai perbedaan bisa terjalin dengan indah dalam satu kesatuan. 

			“Perpaduan tari-tarian dari berbagai daerah yang ditampilkan, mengajarkan pada kita bahwa kebudayaan mengajarkan keindahan 
			
			jika kita persatukan,” tegas Anas. Dalam momen tersebut, Anas juga mengajak seluruh elemen untuk berdoa bersama untuk kesatuan dan 
			
			kedamaian bangsa Indonesia. “Kita senantiasa berdoa kepada Tuhan Yang Maha Esa agar Negara kita ini tetap aman dan damai, dijauhkan 
			
			dari berbagai disintegrasi bangsa,” pintanya. Selain disuguhkan dengan sendratari dan lantunan lagu-lagu daerah, Nusantara Bersatu 
			
			di Banyuwangi juga diisi dengan deklamasi puisi oleh perwakilan pelajar dari latar belakang agama yang berbeda.

			Masing-masing dari mereka membawakan puisi bertemakan persatuan dan toleransi antar sesama anak bangsa. 
			
			Sebelumnya, para hadirin juga membubuhkan tanda tangan di atas kain putih sepanjang sepuluh meter sebagai bentuk komitmen untuk 
			
			menjaga persatuan bangsa. </P>  </tr>
