			<td  align=center> <h1> Indahnya Sungai Kalilo Banyuwangi, Bersih dan Berwarna-warni </h1> </td>  
			 <tr><td>  
			 <center> <img src="IMG/kalilo.jpg"> </center>
			
			 <p>  &nbsp; &nbsp; Sungai Kalilo, Kelurahan Singonegaran, Kecamatan Banyuwangi, Kabupaten Banyuwangi, kini tampak berbeda.

			Selain bersih, sepanjang bantaran sungai kini berwarna-warni.

			Pelengsengan sungai dan tembok-tembok rumah yang menghadap ke sungai kini berwarna-warni. Ini bisa menjadi obyek wisata baru.

			Ini merupakan bentuk kerja sama antara Ikatan Alumni Institut Teknologi Sepuluh November (IKA-ITS) Surabaya dan Pemkab Banyuwangi.

			Sekitar 90 alumni-alumni ITS, masyarakat sekitar, dan Pemkab Banyuwangi, mengecat tembok dan pelengsengan sungai, Sabtu (12/11/2016).

			Mereka bergotong royong membuat sungai lebih indah dan sedap untuk dipandang. <br> <br>

			&nbsp; &nbsp; Melalui program Banyuwangi Berwarna, mereka bekerja sama membuat sungai bersih dan berwarna-warni.

			“Ini merupakan sebagian kecil dari MoU antara IKA-ITS bersama Pemkab Banyuwangi,” kata Guntur Priambodo, salah satu anggota IKA-ITS.

			Sungai Kalilo memiliki sejarah bagi Banyuwangi. Banyak lagu-lagu Banyuwangi yang terinspirasi dari Sungai Kalilo.

			Kalilo merupakan sungai yang mengalir di pusat Kota Banyuwangi. Kalilo sangat fenomenal karena lokasinya yang sangat strategis dan fungsinya di masa lalu.

			Berdasarkan cerita masyarakat, Kalilo berasal dari kata kali dan elo. Kali adalah sungai, sedangkan Elo merupakan nama pohon yang dulu banyak tumbuh di Kelurahan Singonegaran dan Pengantigan.

			Menurut Guntur, dengan program ini akan mengubah maindset masyarakat. Sehingga membuat masyarakat turut menjaga kebersihan dan keindahan sungai.

			“Apabila ada yang membuang sampah ke sungai, nantinya bisa ditegur masyarakat sekitar. Karena eman-eman sudah bagus,” kata Guntur. <br> <br>

			&nbsp; &nbsp; Sungai Kalilo memiliki panjang dari hulu ke muara sepanjang 7 kilometer.

			Guntur mengatakan, kerja sama dengan IKA-ITS memudahkan untuk membuat sungai lebih bersih dan indah. Karena IKA-ITS memiliki stakeholder yang mumpuni untuk mengatasi sampah dan limbah domestik.

			Selanjutnya, menurut Guntur akan diupayakan untuk membuat sumur resapan atau septi tank secara masif di sepanjang Sungai Kalilo untuk mengatasi limbah domestik.

			Sehingga saluran-saluran pembuangan dari rumah penduduk yang langsung sungai bisa diganti.

			“Dipercantik dulu, agar mindset masyarakat bisa berubah,” kata Guntur yang juga Kepala Dinas PU Pengairan. <br> <br>

			&nbsp; &nbsp; Tidak hanya itu, nantinya juga akan dibangun selfie area agar masyarakat dan pengunjung bisa selfie dengan background sungai yang warna-warni.

			“Konsepnya keroyokan. Di IKA ITS banyak alumni dari berbagai disiplin ilmu,” kata Guntur.

			Wakil Dekan, Fakultas Teknik Sipil Lingkungan Warma Dewanti mengatakan, bakti sosial ini merupakan upaya kerja sama untuk meningkatkan lingkungan di sepanjang bantaran sungai Kalilo.

			“Ini masuk dalam bidang sanitasi dan persampahan,” kata Warma.

			IKA-ITS juga memberikan bantuan berupa alat-alat kebersihan pada masyarakat di sekitar Kalilo. Harapannya, masyarakat juga turut berperan aktif untuk menjaga kebersihan dan keindahan sungai.

			Bupati Banyuwangi Abdullah Azwar Anas mengatakan, kerja sama dengan ITS sudah lama terjalin dan merupakan kelanjutan dari kerja sama itu.

			“Bagi Banyuwangi, keterlibatan kampus-kampus negeri seperti ITS merupakan energi positif. Karena mereka punya tenaga ahli dan pengalaman. Sehingga mereka bisa melakukan percepatan-percepatan di Banyuwangi,” kata Anas.

			Kedatangan IKA ITS ke Banyuwangi, selain melakukan MoU bersama Banyuwangi, juga ada kegiatan sosial dengan mengecat dan membersihkan sepanjang bantaran Sungai Kalilo.

			“Selain itu juga mereka akan menyaksikan Banyuwangi Ethno Carnival,” kata Anas. </P>  </tr>
