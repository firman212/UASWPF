			<td  align=center> <h1> Dua Siswa Banyuwangi Ini Olah Limbah Tahu Pakai Kelor dan Pisang </h1> </td>  
			 <tr><td>  
			 <center> <img src="IMG/penelitian.jpg"> </center>
			
			 <p>  &nbsp; &nbsp; Terinspirasi dari polusi yang timbul dari limbah air bekas
				pembuatan tahu, dua orang siswa asal SMA Negeri 1
				
				Purwoharjo, Banyuwangi berkreasi untuk
				
				menjernihkannya. Mereka mengolah suatu serbuk yang
				
				bisa menetralisir dan mengurangi kadar polutan yang
				
				ditimbulkan limbah air tahu dengan menggunakan bahan
				
				alam, yakni biji buah kelor dan kulit pisang. <br> <br>

				&nbsp; &nbsp; MKedua siswa tersebut adalah Alif Alfian Surur, siswa kelas
				
				XII, dan adik kelasnya, M. Yogie Hendrawan. “Awalnya,
				
				saat bermain di rumah paman yang jadi pengusaha tahu.
				
				Di sana saya mendengar banyak tetangga yang mengeluh dengan bau limbahnya,” tutur Alif. 
				
				“Di dekat sekolah juga ada. Baunya terkadang sampai juga ke dalam kelas,” imbuh Yogi. <br> <br>

				&nbsp; &nbsp; Dalam penelitiannya, ternyata limbah tahu cukup menjadi permasalahan yang kompleks dalam pencemaran air bersih.
				
				Mengutip dari data Kementerian Lingkungan Hidup, tiap harinya limbah air tahu yang diproduksi bisa mencapai 1.000 hingga 4.000 liter / hari.

				“Bisa dibayangkan betapa banyaknya jika diakumulasi dalam waktu satu tahun,” pekik Yogie <br> <br>

				&nbsp; &nbsp; Dari permasalahan tersebut, lantas kedua siswa yang tergabung dalam ektrakulikuler karya tulis ilmiah di sekolahnya itu,

				melakukan studi pustaka dan serangkaian eksperimen. “Dalam jurnal yang diterbitkan oleh Universitas Leicester, Inggris, 
				
				disana terdapat sebuah penelitian tentang penggunaan biji kelor (Moringa oliefera) yang dapat membunuh bakteri Escherichia coli. 
				
				Dari situ, akhirnya kami berpikir untuk menerapkannya dalam limbah tahu,” ungkap Alif. <br> <br>
				
				&nbsp; &nbsp; Tidak hanya memanfaatkan biji kelor, di bawah bimbingan guru biologinya, kedua siswa asal Kecamatan Cluring,

				juga memadukannya dengan ekstrak kulit pisang kepok (Musa acuminata bilbisiana coli). <br> <br>
				
				&nbsp; &nbsp; “Sengaja kami pilih kulit pisang kepok. Selain karena memiliki kandungan Asam Karboksilat yang paling tinggi,

				juga banyak kita temui pada pedagang pisang goreng. Dimana tumpukkan kulitnya hanya menjadi sampah, limbah,
				
				yang tidak dimanfaatkan. Jadi double manfaatnya,” ungkap Alif. <br> <br>
				
				&nbsp; &nbsp; Lalu, kedua siswa tersebut mengekstrak biji kelor dan kulit pisang kepok tersebut menjadi serbuk.

				Dengan menggunakan metode koagulasi/flokulasi, serbuk tersebut dilarutkan ke dalam air limbah tahu. 
				
				“Untuk hasil yang maksimal, satu liter limbah air tahu bisa jernih hanya dengan satu gram serbuk (biji kelor dan kulit pisang kepok),” imbuh Alif. <br> <br>
				
				&nbsp; &nbsp; “Kemudian, setelah serbuk tersebut dilarutkan ke dalam limbah air tahu, diaduk terlebih dahulu hingga 15 menit. Setelah itu, dibiarkan hingga sekitar empat jam.

				Setelah itu, limbah akan mengendap dan air kembali jernih. Sehingga aman jika untuk dibuang kembali ke sungai dan tidak mengakibatkan bau lagi,” papar Yogie. <br> <Br>

				&nbsp; &nbsp;Tak ayal, karya kedua siswa tersebut berhasil meraih prestasi. Dalam lomba karya tulis ilmiah yang diselenggarakan oleh Universitas Indonesia 
				
				Program Studi Ilmu Lingkungan dalam rangka Homecoming day 35 tahun untuk pelajar SMP dan SMA, karya mereka menggondol dua penghargaan. 
				
				“Alhmadulillah, karya kami mendapatkan penghargaan sebagai pameran terbaik dan juga poster terbaik,” cetus Yogie. <br> <br>
				
				&nbsp; &nbsp; Namun, tutur Alif, penelitian yang dilakukan selama satu bulan lebih itu, tidak semata untuk memenangkan perlombaan saja. 
				
				“Yang terpenting dari penelitian ini adalah agar kami lebih peka terhadap permasalahan lingkungan,” cetusnya.<br> <br>
				
				Ke depannya, keduanya juga akan melakukan serangkaian penelitian lanjutan. Di antaranya adalah mengelola endapan

				hasil penetralisiran limbah air tahu tersebut. “Kita akan terus mengembangkannya,” ungkap keduanya.  </P>  </tr>
